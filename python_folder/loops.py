# while loop - print 1-20 numbers

i=1
while i<=20:
    print(f"The value of i is: {i}")
    i=i+1

# for loop - print 1-10 numbers

for j in range(1,11):
    print(f"The value of j is: {j}")


for k in range(0,20,2):  #even numbers
    print(k)

for a in range(1,20,2):  #odd numbers
    print(a)